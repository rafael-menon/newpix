CREATE TABLE IF NOT EXISTS users (
    user_id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(120) NOT NULL,
    cpf VARCHAR(14) NOT NULL UNIQUE,
    password VARCHAR(120) NOT NULL,
    balance DECIMAL(10, 2) NOT NULL DEFAULT 0.00,
    token VARCHAR(200)
);

